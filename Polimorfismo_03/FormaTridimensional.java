public class FormaTridimensional extends Forma{
    private int x, y, z;

    public FormaTridimensional(int k, int l, int m){
        x = k;
        y = l;
        z = m;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public void setZ(int z) {
        this.z = z;
    }
}
